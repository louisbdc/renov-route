      <p>
        Le pictogramme PMR parking doit mesurer au minimum 0,50 m x 0,60 m. Cependant, pour une visibilite optimale, une dimension de 1,00 m x 1,20 m est recommandee. Le pictogramme doit etre en blanc sur fond bleu et place au centre ou a l&apos;avant de la place, visible depuis l&apos;allee de circulation.
      </p>

